#include "libft.h"

size_t	ft_nbrlen(int c)
{
	size_t count;

	count = 0;
	if (c == 0)
		return (1);
	if (c < 0)
		count++;
	while (c != 0)
	{
		c = c / 10;
		count++;
	}
	return (count);
}
